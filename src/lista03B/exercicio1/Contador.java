package lista03B.exercicio1;

public class Contador {
    public static void main(String[] args) {
        System.out.println("Contaremos de 1 a 100");
        for (int i = 1; i<101; i++){
            System.out.println(i);
        }

        System.out.println("Vamos contar mais uma vez");
        int i=1;
        while (i < 101){
            System.out.println(i);
            i++;

        }
    }
}
